set<MapPoint*> KeyFrame::GetMapPoints()
{
    unique_lock<mutex> lock(mMutexFeatures);
    set<MapPoint*> s;
    for(size_t i=0, iend=mvpMapPoints.size(); i<iend; i++)
    {
        if(!mvpMapPoints[i]) //如果这个地图点为空
            continue;
        MapPoint* pMP = mvpMapPoints[i];
        if(!pMP->isBad())
            s.insert(pMP);
    }
    return s;
}

// 返回该关键帧里面，被跟踪的特征点个数，也就是有对应好的地图点的个数
int KeyFrame::TrackedMapPoints(const int &minObs)
{
    unique_lock<mutex> lock(mMutexFeatures);

    int nPoints=0;
    const bool bCheckObs = minObs>0;
    for(int i=0; i<N; i++) //对于关键帧中的所有关键点
    {
        MapPoint* pMP = mvpMapPoints[i]; //关键点对应的地图点
        if(pMP)
        {
            if(!pMP->isBad())
            {
                if(bCheckObs) // 表示需要验证观测到该地图点的关键帧的数量
                {
                    if(mvpMapPoints[i]->Observations()>=minObs)
                        nPoints++;
                }
                else
                    nPoints++;
            }
        }
    }

    return nPoints;
}

