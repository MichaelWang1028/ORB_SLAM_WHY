#ifndef MAP_H
#define MAP_H

#include "MapPoint.h"
#include "KeyFrame.h"
#include <set>

#include <mutex>



namespace ORB_SLAM2
{

class MapPoint;
class KeyFrame;

class Map
{
public:
    Map();

    void AddKeyFrame(KeyFrame* pKF);
    void AddMapPoint(MapPoint* pMP);
    void EraseMapPoint(MapPoint* pMP);
    void EraseKeyFrame(KeyFrame* pKF);
    void SetReferenceMapPoints(const std::vector<MapPoint*> &vpMPs);
    void InformNewBigChange();
    int GetLastBigChangeIdx();

    std::vector<KeyFrame*> GetAllKeyFrames();
    std::vector<MapPoint*> GetAllMapPoints();
    std::vector<MapPoint*> GetReferenceMapPoints();

    long unsigned int MapPointsInMap(); // 地图中地图点的个数
    long unsigned  KeyFramesInMap(); // 地图中关键帧的个数

    long unsigned int GetMaxKFid(); // 返回最大关键帧的ID

    void clear();

    vector<KeyFrame*> mvpKeyFrameOrigins;

    std::mutex mMutexMapUpdate;

    // This avoid that two points are created simultaneously in separate threads (id conflict)
    std::mutex mMutexPointCreation; // 因为在多个线程里都有可能创建地图点，所以需要对地图点的ID利用互斥锁锁住

protected:
    std::set<MapPoint*> mspMapPoints; //存放地图中的地图点
    std::set<KeyFrame*> mspKeyFrames; //存放地图中的关键帧

    std::vector<MapPoint*> mvpReferenceMapPoints;  //存放的是当前图像帧的局部地图点，主要用来显示的时候使用

    long unsigned int mnMaxKFid;

    // Index related to a big change in the map (loop closure, global BA)
    int mnBigChangeIdx;

    std::mutex mMutexMap;
};

} //namespace ORB_SLAM

#endif // MAP_H
